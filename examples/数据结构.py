# -*- coding: utf-8 -*-
"""中文数据结构操作示例：列表、字典、集合、元组"""


# ============ 列表 ============

print("【列表操作】")
水果 = ["苹果", "香蕉", "橙子", "葡萄", "西瓜"]

print(f"所有水果：{水果}")
print(f"第一个：{水果[0]}")
print(f"最后两个：{水果[-2:]}")

水果.append("芒果")
print(f"添加芒果后：{水果}")

水果.remove("香蕉")
print(f"删除香蕉后：{水果}")

print(f"水果数量：{len(水果)}")

# 列表推导式
长度列表 = [len(名) for 名 in 水果]
print(f"各水果名长度：{长度列表}")


# ============ 字典 ============

print("\n【字典操作】")
价格表 = {
    "苹果": 5.5,
    "香蕉": 3.0,
    "橙子": 4.2,
    "葡萄": 8.8,
    "西瓜": 12.0,
}

print("水果价格表：")
for 水果名, 价格 in 价格表.items():
    print(f"  {水果名}: {价格} 元/斤")

# 字典推导式
打折表 = {名: 价 * 0.8 for 名, 价 in 价格表.items()}
print(f"\n八折优惠：{打折表}")

# 安全取值
芒果价格 = 价格表.get("芒果", "暂无")
print(f"芒果价格：{芒果价格}")


# ============ 集合 ============

print("\n【集合操作】")
班级A = {"小明", "小红", "小华", "小李"}
班级B = {"小红", "小李", "小张", "小王"}

print(f"A班：{班级A}")
print(f"B班：{班级B}")
print(f"两班都有：{班级A & 班级B}")        # 交集
print(f"所有学生：{班级A | 班级B}")        # 并集
print(f"仅A班有：{班级A - 班级B}")         # 差集


# ============ 元组 ============

print("\n【元组操作】")
坐标 = (116.4, 39.9)
城市 = ("北京", "中国", 2154)

print(f"坐标：{坐标}")
print(f"城市信息：{城市[0]}，{城市[1]}，人口约{城市[2]}万")

# 元组解包
经度, 纬度 = 坐标
print(f"经度={经度}, 纬度={纬度}")
