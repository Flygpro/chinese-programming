# -*- coding: utf-8 -*-
"""猜数字小游戏 —— 全中文实现"""

import random as 随机


def 猜数字游戏(最小值=1, 最大值=100, 最大尝试次数=7):
    """猜数字游戏主逻辑"""
    目标数字 = 随机.randint(最小值, 最大值)
    尝试次数 = 0

    print(f"""
╔══════════════════════════════╗
║      🎯 猜数字游戏           ║
╠══════════════════════════════╣
║  我想了一个 {最小值}~{最大值} 的数字    ║
║  你有 {最大尝试次数} 次机会猜中它！       ║
╚══════════════════════════════╝
""")

    while 尝试次数 < 最大尝试次数:
        剩余次数 = 最大尝试次数 - 尝试次数

        try:
            用户猜测 = int(input(f"🎯 第 {尝试次数 + 1} 次猜测（剩余 {剩余次数} 次）> "))
        except ValueError:
            print("   ⚠️  请输入一个有效的整数！")
            continue
        except (EOFError, KeyboardInterrupt):
            print(f"\n👋 游戏结束，答案是 {目标数字}")
            return

        尝试次数 += 1

        if 用户猜测 == 目标数字:
            print(f"""
🎉🎉🎉 恭喜你猜对了！
   答案就是 {目标数字}
   你用了 {尝试次数} 次猜中
   评级：{"⭐" * max(1, 最大尝试次数 - 尝试次数 + 1)}
""")
            return
        elif 用户猜测 < 目标数字:
            print("   📈 太小了！再大一点。")
        else:
            print("   📉 太大了！再小一点。")

    print(f"""
😢 很遗憾，机会用完了！
   正确答案是：{目标数字}
   再试一次吧！
""")


# ============ 主程序 ============

if __name__ == "__main__":
    print("欢迎来到猜数字游戏！")

    while True:
        猜数字游戏()

        try:
            再玩 = input("🔄 再玩一次？(是/否) > ").strip()
        except (EOFError, KeyboardInterrupt):
            break

        if 再玩 not in ("是", "y", "yes", "好", "对"):
            print("👋 感谢游玩，再见！")
            break
